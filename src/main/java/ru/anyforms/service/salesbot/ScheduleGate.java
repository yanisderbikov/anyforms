package ru.anyforms.service.salesbot;

import java.time.Instant;

/**
 * Решает, должен ли текущий тик шедулера запустить прогон.
 * <p>
 * Инкапсулирует расписание ({@code schedule}), джиттер и защиту «слот сегодня уже
 * отрабатывал». Шедулер-тикер не знает деталей расписания — только спрашивает гейт.
 */
public interface ScheduleGate {

    /**
     * Проверяет, попадает ли {@code now} в активный слот, который сегодня ещё не запускался,
     * и если да — атомарно помечает слот как отработавший сегодня (чтобы повторные тики
     * в ту же минуту не запускали прогон дважды).
     *
     * @param now текущий момент (UTC)
     * @return {@code true}, если прогон нужно запустить.
     */
    boolean tryClaimDueSlot(Instant now);
}
