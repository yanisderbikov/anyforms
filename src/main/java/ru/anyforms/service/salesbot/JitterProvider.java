package ru.anyforms.service.salesbot;

import java.time.Duration;
import java.time.Instant;

/**
 * «Человечность времени»: небольшой сдвиг к базовому времени слота, чтобы фактическое
 * время запуска чуть «плавало» от недели к неделе.
 * <p>
 * Контракт: для пары (слот, день) джиттер ДЕТЕРМИНИРОВАН (стабилен в течение суток),
 * но меняется день ото дня. Это позволяет тикеру каждую минуту устойчиво сравнивать
 * текущее время с эффективным временем слота без «дребезга».
 */
public interface JitterProvider {

    /**
     * @param slotId ID слота расписания
     * @param dayUtc начало суток (UTC), для которых считается сдвиг (Instant, усечённый до дня)
     * @return детерминированный для данных (slotId, dayUtc) сдвиг к базовому времени слота
     */
    Duration jitterFor(long slotId, Instant dayUtc);
}
