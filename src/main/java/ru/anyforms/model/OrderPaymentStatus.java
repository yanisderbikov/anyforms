package ru.anyforms.model;

/**
 * Статус онлайн-оплаты заказа. Заказ маркетплейса создаётся сразу при чекауте
 * (AWAITING_PAYMENT) и переводится в PAID вебхуком Юкассы; заказы из АМО и
 * созданные вручную оплату через сайт не проходят — у них NONE.
 * В рабочие списки цеха попадают только NONE и PAID.
 */
public enum OrderPaymentStatus {
    /** Не онлайн-оплата (АМО-синк, кастомные заказы). */
    NONE,
    /** Создан на чекауте, ждёт оплату (брошенная корзина, пока не оплачен). */
    AWAITING_PAYMENT,
    /** Оплачен онлайн. */
    PAID,
    /** Платёж отменён. */
    CANCELED
}
