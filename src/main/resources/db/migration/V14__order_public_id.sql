--  V14 — публичный номер заказа для клиента.
--    orders.public_id — 6 случайных символов A-Z/0-9 (заглавные), уникальный.
--    Отдаётся наружу (страница успеха, письмо, поддержка) вместо внутреннего id.
--    Заполняется только у заказов маркетплейса; у АМО/кастомных — NULL
--    (в Postgres UNIQUE допускает несколько NULL).
-- ============================================================================

ALTER TABLE orders
    ADD COLUMN public_id VARCHAR(6);

CREATE UNIQUE INDEX idx_orders_public_id ON orders (public_id);
