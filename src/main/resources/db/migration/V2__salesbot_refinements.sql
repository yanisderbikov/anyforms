--   1) schedule.time_utc: TIME -> TIMESTAMPTZ, т.к. поле маппится на java.time.Instant.
--      Значима только UTC-составляющая времени суток; дата — плейсхолдер 1970-01-01.
--   2) Сид расписания (некруглые времена; на рантайме добавляется джиттер ±10 мин).
